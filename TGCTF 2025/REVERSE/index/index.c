void xor_key(char* key) {
	int len = strlen(key);
	for (int i = 0; i < len; i++) {
		key[i] = (key[i] ^ 0x51) & 0xFF;
	}
}

void shuffle(char* array, int n) {
	if (n > 1) {
		for (int i = 0; i < n - 1; i++) {
			int j = i + rand() / (0x7fff / (n - i) + 1);
			/*printf("%d\n", j);*/
			char t = array[j];
			array[j] = array[i];
			array[i] = t;
		}
	}
}

